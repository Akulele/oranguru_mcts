#!/usr/bin/env bash
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT"

PYTHON_BIN="${PYTHON_BIN:-$ROOT/venv/bin/python}"
NAME="${NAME:-efficient_worldrank_ms400}"
BLOCKS="${BLOCKS:-4}"
BATTLES_PER_BLOCK="${BATTLES_PER_BLOCK:-500}"
MAX_PARALLEL="${MAX_PARALLEL:-1}"
RETRY_PARTIAL="${RETRY_PARTIAL:-2}"
FOULPLAY_USERNAME_BASE="${FOULPLAY_USERNAME_BASE:-fpb17effwr}"
SUMMARY_OUT="${SUMMARY_OUT:-logs/evals/efficient_worldrank_ms400_blocks.json}"
FORMAT="${FORMAT:-gen9randombattle}"
PROGRESS_EVERY="${PROGRESS_EVERY:-10}"
FOULPLAY_SEARCH_MS="${FOULPLAY_SEARCH_MS:-120}"
FOULPLAY_PARALLELISM="${FOULPLAY_PARALLELISM:-1}"
FOULPLAY_WAIT="${FOULPLAY_WAIT:-120}"
BATTLE_TIMEOUT="${BATTLE_TIMEOUT:-420}"
FOULPLAY_RETRIES="${FOULPLAY_RETRIES:-30}"
WS_URI="${WS_URI:-ws://127.0.0.1:8000/showdown/websocket}"
FOULPLAY_PASSWORD="${FOULPLAY_PASSWORD:-foulplay_pass}"
WORLD_RANKER_CHECKPOINT="${WORLD_RANKER_CHECKPOINT:-checkpoints/rl/world_ranker_g6_server_v3.pt}"
WORLD_RANKER_DEVICE="${WORLD_RANKER_DEVICE:-cpu}"

exec "$PYTHON_BIN" evaluation/run_block_eval.py \
  --python "$PYTHON_BIN" \
  --name "$NAME" \
  --blocks "$BLOCKS" \
  --battles-per-block "$BATTLES_PER_BLOCK" \
  --max-parallel "$MAX_PARALLEL" \
  --retry-partial "$RETRY_PARTIAL" \
  --foulplay-username-base "$FOULPLAY_USERNAME_BASE" \
  --summary-out "$SUMMARY_OUT" \
  --env PYTHONUNBUFFERED=1 \
  --env ORANGURU_EVAL_MODE=1 \
  --env ORANGURU_SHOWDOWN_WS="$WS_URI" \
  --env ORANGURU_DECISION_DIAG=1 \
  --env ORANGURU_BELIEF_SAMPLING=1 \
  --env ORANGURU_MCTS_DETERMINISTIC=1 \
  --env ORANGURU_MCTS_DETERMINISTIC_EVAL_ONLY=1 \
  --env ORANGURU_UNLIKELY_CHOICE_INFER=1 \
  --env ORANGURU_PARALLELISM=8 \
  --env ORANGURU_SAMPLE_STATES=8 \
  --env ORANGURU_SEARCH_MS=400 \
  --env ORANGURU_LOW_UNCERTAINTY_WORLD_REDUCTION=0 \
  --env ORANGURU_ENDGAME_WORLD_REDUCTION=0 \
  --env ORANGURU_AUTO_TERA=0 \
  --env ORANGURU_HEURISTIC_BLEND=0.0 \
  --env ORANGURU_MIN_HEURISTIC_BLEND=0.0 \
  --env ORANGURU_SELECTION_MODE=blend \
  --env ORANGURU_GATE_MODE=hard \
  --env ORANGURU_MCTS_CONFIDENCE=0.3 \
  --env ORANGURU_STATUS_KO_GUARD=0 \
  --env ORANGURU_ANTI_SWITCH_CHURN=0 \
  --env ORANGURU_RL_PRIOR=0 \
  --env ORANGURU_SEARCH_PRIOR=0 \
  --env ORANGURU_SWITCH_PRIOR=0 \
  --env ORANGURU_PASSIVE_BREAKER=0 \
  --env ORANGURU_TERA_PRUNER=0 \
  --env ORANGURU_ADAPTIVE_FALLBACK=0 \
  --env ORANGURU_WORLD_RANKER=1 \
  --env ORANGURU_WORLD_RANKER_CHECKPOINT="$WORLD_RANKER_CHECKPOINT" \
  --env ORANGURU_WORLD_RANKER_DEVICE="$WORLD_RANKER_DEVICE" \
  --env ORANGURU_WORLD_RANKER_KEEP_TOPK=12 \
  --env ORANGURU_WORLD_RANKER_MIN_CANDIDATES=14 \
  --env ORANGURU_WORLD_RANKER_LOW_UNCERTAINTY_ONLY=0 \
  --env ORANGURU_WORLD_RANKER_ENDGAME_ONLY=1 \
  --env ORANGURU_SWITCH_MIN_GAIN=0.22 \
  --env ORANGURU_SWITCH_DANGER_IMPROVEMENT=35 \
  --env ORANGURU_SWITCH_HAZARD_MULT=1.35 \
  --env ORANGURU_SWITCH_STREAK_PENALTY=0.18 \
  --env ORANGURU_SWITCH_LOW_GAIN_PENALTY=1.25 \
  --env ORANGURU_TEMPO_PRESSURE_REPLY=180 \
  --env ORANGURU_TEMPO_PRESSURE_MATCHUP=-0.05 \
  --env ORANGURU_TEMPO_DAMAGE_RATIO=0.45 \
  --env ORANGURU_TEMPO_KO_BONUS=55 \
  --env ORANGURU_TEMPO_STRONG_HIT_BONUS=26 \
  --env ORANGURU_TEMPO_PASSIVE_PENALTY=0.72 \
  -- \
  --player oranguru_engine \
  --format "$FORMAT" \
  --progress-every "$PROGRESS_EVERY" \
  --foulplay-python "$PYTHON_BIN" \
  --foulplay-password "$FOULPLAY_PASSWORD" \
  --foulplay-search-ms "$FOULPLAY_SEARCH_MS" \
  --foulplay-parallelism "$FOULPLAY_PARALLELISM" \
  --foulplay-wait "$FOULPLAY_WAIT" \
  --battle-timeout "$BATTLE_TIMEOUT" \
  --foulplay-retries "$FOULPLAY_RETRIES" \
  --foulplay-challenges \
  --debug \
  --ws-uri "$WS_URI" \
  "$@"
