#!/usr/bin/env bash
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT"

PYTHON_BIN="${PYTHON_BIN:-$ROOT/venv/bin/python}"
PLAYER="${PLAYER:-oranguru_engine}"
FORMAT="${FORMAT:-gen9randombattle}"
BATTLES="${BATTLES:-500}"
PROGRESS_EVERY="${PROGRESS_EVERY:-10}"
FOULPLAY_SEARCH_MS="${FOULPLAY_SEARCH_MS:-120}"
FOULPLAY_PARALLELISM="${FOULPLAY_PARALLELISM:-1}"
FOULPLAY_WAIT="${FOULPLAY_WAIT:-120}"
BATTLE_TIMEOUT="${BATTLE_TIMEOUT:-420}"
FOULPLAY_RETRIES="${FOULPLAY_RETRIES:-30}"
WS_URI="${WS_URI:-ws://127.0.0.1:8000/showdown/websocket}"
FOULPLAY_PASSWORD="${FOULPLAY_PASSWORD:-foulplay_pass}"
FOULPLAY_USERNAME="${FOULPLAY_USERNAME:-fpb17strength}"

exec env \
  PYTHONUNBUFFERED=1 \
  ORANGURU_EVAL_MODE=1 \
  ORANGURU_SHOWDOWN_WS="$WS_URI" \
  ORANGURU_DECISION_DIAG=1 \
  ORANGURU_BELIEF_SAMPLING=1 \
  ORANGURU_MCTS_DETERMINISTIC=1 \
  ORANGURU_MCTS_DETERMINISTIC_EVAL_ONLY=1 \
  ORANGURU_UNLIKELY_CHOICE_INFER=1 \
  ORANGURU_PARALLELISM=8 \
  ORANGURU_SAMPLE_STATES=8 \
  ORANGURU_SEARCH_MS=400 \
  ORANGURU_LOW_UNCERTAINTY_WORLD_REDUCTION=0 \
  ORANGURU_ENDGAME_WORLD_REDUCTION=0 \
  ORANGURU_AUTO_TERA=0 \
  ORANGURU_HEURISTIC_BLEND=0.0 \
  ORANGURU_MIN_HEURISTIC_BLEND=0.0 \
  ORANGURU_SELECTION_MODE=blend \
  ORANGURU_GATE_MODE=hard \
  ORANGURU_MCTS_CONFIDENCE=0.3 \
  ORANGURU_STATUS_KO_GUARD=0 \
  ORANGURU_ANTI_SWITCH_CHURN=0 \
  ORANGURU_RL_PRIOR=0 \
  ORANGURU_SEARCH_PRIOR=0 \
  ORANGURU_SWITCH_PRIOR=0 \
  ORANGURU_PASSIVE_BREAKER=0 \
  ORANGURU_TERA_PRUNER=0 \
  ORANGURU_WORLD_RANKER=0 \
  ORANGURU_ADAPTIVE_FALLBACK=0 \
  ORANGURU_SWITCH_MIN_GAIN=0.22 \
  ORANGURU_SWITCH_DANGER_IMPROVEMENT=35 \
  ORANGURU_SWITCH_HAZARD_MULT=1.35 \
  ORANGURU_SWITCH_STREAK_PENALTY=0.18 \
  ORANGURU_SWITCH_LOW_GAIN_PENALTY=1.25 \
  ORANGURU_TEMPO_PRESSURE_REPLY=180 \
  ORANGURU_TEMPO_PRESSURE_MATCHUP=-0.05 \
  ORANGURU_TEMPO_DAMAGE_RATIO=0.45 \
  ORANGURU_TEMPO_KO_BONUS=55 \
  ORANGURU_TEMPO_STRONG_HIT_BONUS=26 \
  ORANGURU_TEMPO_PASSIVE_PENALTY=0.72 \
  "$PYTHON_BIN" evaluation/eval_vs_foulplay.py \
    --player "$PLAYER" \
    --battles "$BATTLES" \
    --format "$FORMAT" \
    --progress-every "$PROGRESS_EVERY" \
    --foulplay-python "$PYTHON_BIN" \
    --foulplay-username "$FOULPLAY_USERNAME" \
    --foulplay-password "$FOULPLAY_PASSWORD" \
    --foulplay-search-ms "$FOULPLAY_SEARCH_MS" \
    --foulplay-parallelism "$FOULPLAY_PARALLELISM" \
    --foulplay-wait "$FOULPLAY_WAIT" \
    --battle-timeout "$BATTLE_TIMEOUT" \
    --foulplay-retries "$FOULPLAY_RETRIES" \
    --foulplay-challenges \
    --debug \
    --ws-uri "$WS_URI" \
    "$@"
